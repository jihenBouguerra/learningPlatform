    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        // System.out.println(jTextField1.getText() + "*********" + jPasswordField1.getText());
        if (jPasswordField1.getText().trim().equals("") || jTextField1.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Veuillez remplir tous les champs ");
        } else {
            connected = new Utilisateur(jTextField1.getText().trim().toLowerCase(),
                    jPasswordField1.getText().trim().toLowerCase());
            try {
                if (jTextField1.getText().trim().toLowerCase().equals("admin") && jPasswordField1.getText().trim().toLowerCase().equals("admin")) {
                    new MenuAdmin().setVisible(true);
                } else if (connected.existeCompte()) {
                    connected = connected.infoCompte();
                    Global.connected = connected;
                    System.out.println(connected.estEtudiant());
                    if (connected.estEtudiant()) {
                        new MenuEtudiant().setVisible(true);
                    } else {
                        new MenuEnseignant().setVisible(true);
                    }
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(null, "VÃ©rifiez votre mot de passe");
                }

            } catch (SQLException ex) {
                Logger.getLogger(wlcPage.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
    }//GEN-LAST:event_jButton1ActionPerformed

