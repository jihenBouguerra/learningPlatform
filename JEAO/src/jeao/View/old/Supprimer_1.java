    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        switch (CSupprimer.getAction()) {
            case "Supprimer Compte": {
                try {
                    CSupprimer.getListeUtilisateur().get(jComboBox1.getSelectedIndex()).supprime();

                } catch (SQLException ex) {
                    Logger.getLogger(Supprimer_1.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            break;
            case "Supprimer Module": {
                try {
                    CSupprimer.getListeModule().get(jComboBox1.getSelectedIndex()).supprimer();
                } catch (SQLException ex) {
                    Logger.getLogger(Supprimer_1.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            break;
            case "Supprimer Niveau d'etude": {
                try {
                    CSupprimer.getListeNivEtude().get(jComboBox1.getSelectedIndex()).supprimer();
                } catch (SQLException ex) {
                    Logger.getLogger(Supprimer_1.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            break;
            case "Supprimer Examen": {
                try {
                    CSupprimer.getListeExamen().get(jComboBox1.getSelectedIndex()).supprimer();
                } catch (SQLException ex) {
                    Logger.getLogger(Supprimer_1.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            break;

        }
        JOptionPane.showMessageDialog(null, "Suppression effectu√©");
        this.dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

