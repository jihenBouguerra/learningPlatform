    private void jList1ValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_jList1ValueChanged
        jList2.clearSelection();
        jList4.clearSelection();
        nom.setText("");
        enseignant.setText("");
        format.setText("");
        type.setText("");
        module.setText("");
        Presentation.setText("");

        int n = jList1.getSelectedIndex();
        jComboBox1.setSelectedIndex(0);
        if (n != -1) {
            DefaultListModel listModel2 = new DefaultListModel();
            DefaultListModel listModel4 = new DefaultListModel();
            try {
                listeSource = listeModules.get(n).getSources();
                listeExamen = listeModules.get(n).getExamens();

                /*System.out.println("aaaa");
                System.out.println(listeExamen.size());
                System.out.println(listeSource.size());*/
                for (int i = 0; i < listeExamen.size(); i++) {

                    listModel2.addElement(listeExamen.get(i).getTitre());
                }
                jList2.setModel(listModel2);

                for (int i = 0; i < listeSource.size(); i++) {
                    listModel4.addElement(listeSource.get(i).getNomFichier());
                }
                jList4.setModel(listModel4);

            } catch (SQLException ex) {
                System.out.print(ex.getMessage());
                Logger.getLogger(MenuEtudiant.class.getName()).log(Level.SEVERE, null, ex);
            }

        }


    }//GEN-LAST:event_jList1ValueChanged

