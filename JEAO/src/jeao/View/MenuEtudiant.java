    private void jList4ValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_jList4ValueChanged

        if (listeSource.isEmpty()) {

        } else if (jList4.getSelectedIndex() != -1) {
            int n = jList4.getSelectedIndex();
            nom.setText(listeSource.get(n).getNomFichier());
            type.setText(listeSource.get(n).getType());
            try {
                module.setText(listeSource.get(n).getModule().getNomComplet());
                enseignant.setText(listeSource.get(n).getEnseigant().getPenom());

            } catch (SQLException ex) {
                Logger.getLogger(MenuEtudiant.class.getName()).log(Level.SEVERE, null, ex);
            }

        }


    }//GEN-LAST:event_jList4ValueChanged

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (listeSource.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Liste Vide");
        } else if (jList4.getSelectedIndex() != -1) {

            String url = listeSource.get(jList4.getSelectedIndex()).getURL();
            try {
                Desktop.getDesktop().open(new File(url));
            } catch (IOException ex) {
                Logger.getLogger(SourceDisponible_1.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Selectionner un source");
        }

    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        examen = new Examen();
        if (jList2.getModel().getSize() != 0) {
            int n = jList2.getSelectedIndex();

            if (n != -1) {
                try {

                    examen = listeExamenNpass.get(n);
                    examen.getListeQuestionBD();
                    Global.examen = examen;

                    if (examen.estQCM()) {
                        new PQuestionQCM().setVisible(true);
                        PQuestionQCM.score = 0;
                        PQuestionQCM.indice = 0;
                    } else {
                        new PQuestion().setVisible(true);

                        PQuestion.indice = 0;
                    }

                } catch (SQLException ex) {
                    Logger.getLogger(MenuEtudiant.class.getName()).log(Level.SEVERE, null, ex);
                    this.dispose();
                }

            }
        }

    }//GEN-LAST:event_jButton1ActionPerformed

